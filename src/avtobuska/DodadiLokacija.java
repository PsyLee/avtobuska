    public void NapolniProverki() throws SQLException
    {
    Vector head = new Vector();
    Vector data = new Vector(1, 1);
    head.add("Име");
        head.add("Град");
        head.add("Улица");
        ResultSet rs = s.ListajLokacii();
        data = new Vector(1, 1);
         while (rs.next()) {
            Vector row = new Vector();
            //IME	ULICA	GRAD
            row.add(rs.getString("IME"));
            row.add(rs.getString("ULICA"));
            row.add(rs.getString("GRAD"));
            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data, head) {
            // zabrana za edit na kolonite
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tabelata.setModel(dtm);
    }
   
