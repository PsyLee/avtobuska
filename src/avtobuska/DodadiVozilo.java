    public void NapolniSoferi() throws SQLException {
        ResultSet rs = s.SoferiKolicina();
        DefaultListModel model = new DefaultListModel();

        while (rs.next()) {
            model.addElement(rs.getString("IME"));
        }
        jList1.setModel(model);

        jList1.setSelectionModel(new DefaultListSelectionModel() {
            @Override
            public void setSelectionInterval(int index0, int index1) {
                if (super.isSelectedIndex(index0)) {
                    super.removeSelectionInterval(index0, index1);
                } else {
                    super.addSelectionInterval(index0, index1);
                }
            }
        });


    }

    public void ComboNapolniGaraza() throws SQLException {
        jComboBox1.addItem("/");

        ResultSet rs = s.GradSoGaraza();
        while (rs.next()) {
            jComboBox1.addItem(rs.getString(1));

        }
        jComboBox1.removeItemAt(0);
    }

    public void NapuniOstanatite() throws SQLException {
        jComboBox2.addItem("/");
        jComboBox3.addItem("/");

        ResultSet rs = s.PuniDropdown();
        while (rs.next()) {
            jComboBox2.addItem(rs.getString(1));
            jComboBox3.addItem(rs.getString(1));
        }
        jComboBox2.removeItemAt(0);
        jComboBox3.removeItemAt(0);
    }

