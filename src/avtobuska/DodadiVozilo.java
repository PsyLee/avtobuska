    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            String sed = sedista.getText();
            int sedistata = Integer.parseInt(sed);

            String Gradot = jComboBox1.getSelectedItem().toString();
            ResultSet rs = s.GradID(Gradot);
            rs.next();
            int gradID = rs.getInt(1);

            AvtobuskaMetodi a = new AvtobuskaMetodi();

            if (servisno.getText().length() == 0) {
                //iza naogjanje na ID na viznite redovi za odredena linija
                rs = s.ZaVozenRed(jComboBox2.getSelectedItem().toString(), jComboBox3.getSelectedItem().toString());
                if (rs.next()) {
                    a.DodadiVozilo(reg.getText(), sedistata, marka.getText(), gorivo.getText(), servisno.getText(), gradID);

                    List<String> listaime = new ArrayList<String>();
                    List<String> listamail = new ArrayList<String>();
                    listaime.addAll(jList1.getSelectedValuesList());

                    ResultSet rs2;
                    for (String temp : listaime) {

                        rs2 = s.mailodSofer(temp);
                        while (rs2.next()) {
                            listamail.add(rs2.getString("mail"));
                        }
                    }

                    for (String temp : listamail) {
                        s.relacijaVozila(reg.getText(), temp);
                    }

                    while (rs.next()) {
                        System.out.println(rs.getInt("VOZENRED_ID"));
                        s.PolniRelZa2(rs.getInt("VOZENRED_ID"), reg.getText());

                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Нема таков возен ред");

                }
            } else {
                s.DodadiVozilo(reg.getText(), sedistata, marka.getText(), gorivo.getText(), servisno.getText(), gradID);

                List<String> listaime = new ArrayList<String>();
                List<String> listamail = new ArrayList<String>();
                listaime.addAll(jList1.getSelectedValuesList());

                ResultSet rs2;
                for (String temp : listaime) {

                    rs2 = s.mailodSofer(temp);
                    while (rs2.next()) {
                        listamail.add(rs2.getString("mail"));
                    }
                }

                for (String temp : listamail) {
                    s.relacijaVozila(reg.getText(), temp);
                }

            }

        } catch (SQLException ex) {
            Logger.getLogger(DodadiVozilo.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

